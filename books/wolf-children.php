            <p>Nuôi những đứa trẻ bình thường đã rất khó, nhưng đối với Hana thì việc này lại càng trở nên khó gấp bội lần khi Yuki và Ame lại là “nửa người nửa sói”.</p>
            <p>Tuy thế, cô lại không hề chối bỏ hay căm ghét phần sói hoang dã chảy trong máu của con mình mà chấp nhận nó, che giấu đi thân phận thực sự của chúng với mọi người, nuôi dạy chúng khôn lớn, dạy cho chúng cách yêu thương,
               hòa nửa người với nửa sói vào làm một.</p>
            <p>Ame và Yuki – Những đứa của sói giống như một lăng kính hiện thực nói lên những vất vả, khó nhọc mà bậc cha mẹ phải trải qua trong suốt quá trình nuôi dưỡng sinh thành, và đồng thời ca ngợi tình cảm gia đình thiêng liêng bất diệt.

Theo đánh giá của mình thì đây là một cuốn Light Novel vô cùng ý nghĩa, giản dị và đầy tính nhân văn cao đẹp.</p>
