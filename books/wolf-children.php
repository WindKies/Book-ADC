            <h1>Review: Những đứa con của sói: Ame và Yuki </h1>
            <p>Được ra mắt lần đầu vào năm 2012, The Wolf Children hay còn được biết đến với cái tên Ame và Yuki – Những đứa con của sói là một bộ phim hoạt hình Nhật Bản được đạo diễn bởi 
              Mamoru Hosoda – người đã tạo nên tuyệt tác Cậu bé và quái vật và từng được đề cử Oscar cho hạng mục phim hoạt hình hay nhất.</p>
            <div class="picture">
                <img src="https://otakugo.net/wp-content/uploads/2018/04/8slb-spdmki-1.jpg" width="550" height="340">
            </div>
           <p>Tuy nhiên, đề tài hôm nay mình muốn gửi đến các bạn không phải là anime The Wolf Children, hay những thành công mà nó đem lại mà là phiên bản Light Novel được đích thân Mamoru Hosoda chuyển thể.
        </p>
        <h2>
        Sơ lược về cốt truyện
        </h2>
            <p>
            Nội dung của câu chuyện xuyên suốt 13 năm ròng rã, xoay quanh cuộc đời của người con gái mang tên Hana từ khi còn là một cô sinh viên đại học, làm quen và phải lòng một “mối tình định mệnh” với chàng sói.
Sau khi kết hôn, Hana đã hạ sinh hai đứa trẻ nửa người nửa sói là Yuki – chị cả, và Ame – cậu em út. Tuy nhiên, cuộc sống êm đềm của gia đình chưa kéo dài được bao lâu thì người cha qua đời để lại cho Hana hai đứa con thơ, cùng với kinh tế của gia đình ngày một khó khăn.
            </p>
           <p>
           Ame và Yuki – Những đứa con của sói không phải là một câu chuyện được kể theo phong cách đấu tranh, sinh tồn hay cuộc hành trình gian nan của những đứa trẻ sói, lạc loài trong thế giới con người, mà là một câu chuyện đầy xúc động về tình cảm gia đình, cụ thể hơn là tình mẫu tử dạt dào, nơi chan chứa những giọt nước mắt cùng với nụ cười hiền từ của người mẹ.
            </p>
