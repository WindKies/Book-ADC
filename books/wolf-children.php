            Có thể nói, trong xuyên suốt tác phẩm, chi tiết Hana kiên cường đứng lên sau cái chết bi thương của người chồng để tập trung nuôi dưỡng hai đứa con thơ của mình lớn khôn đã cho ta thấy cô là một người mẹ kiên cường, đầy nghị lực và giàu lòng trắc ẩn.
            </p>
           
