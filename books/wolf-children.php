            <h2>
             Cảm nhận khi đọc
        </h2>
            <p>
            Giống như bao câu chuyện thần tiên về mối duyên giữa người – thú, mở đầu Ame và Yuki – Những đứa con của sói là mối tình kì lạ giữa Hana với một chàng sói tốt bụng.
Từ xưa đến nay, trong những câu chuyện cổ tích mà ta vẫn thường nghe hồi nhỏ, sói luôn được biết đến là một nhân vật phản diện độc ác, đầy mưu mô, xảo quyệt và phải nhận một kết cục cay đắng là chết trong sự xua đuổi, căm ghét của mọi người.
            </p>
            <p>
            Chính vì thế, hình ảnh chàng sói trong Những đứa con của sói là hình ảnh ẩn dụ tượng trưng cho mặt khác biệt mà cả xã hội chối bỏ.
Nhưng mặc cho điều đó, Hana vẫn sẵn sàng chấp nhận, dám từ bỏ tất cả để đến với anh, yêu thương anh một cách vô điều kiện và cũng chính tình yêu ấy đã tạo động lực giúp cô mạnh mẽ đứng lên sau khi anh qua đời.
        </p>

